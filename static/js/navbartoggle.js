$(function(){
  $("#navbarDropdown").click(function(){
    $(".dropdown-menu").show();
  });
  $("#navbarDropdown").dbClick(function(){
    $(".dropdown-menu").hide();
  });
  $(".accordion-button").click(function(){
    $(".accordion").addClass("open");
  });
  $(".accordion-button").dbclick(function(){
    $(".accordion").removeClass("open");
  });
});
